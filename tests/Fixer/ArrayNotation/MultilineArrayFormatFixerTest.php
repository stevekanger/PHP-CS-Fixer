    {
        yield from self::withAlternateSyntaxCases(
            'Long Array',
            [
                '[' => 'array(',
                ']' => ')',
            ],
            [
                'Single line array remains untouched.' => [
                    <<<'EXPECTED'
                        <?php
                        $arr = [ 1, 2, 3 ];
                        EXPECTED,
                ],

                'Single line nested array remains untouched.' => [
                    <<<'EXPECTED'
                        <?php
                        $arr = [ "one" => [ 1, 2 ] ];
                        EXPECTED,
                ],

                'Multiline array gets formatted.' => [
                    <<<'EXPECTED'
                        <?php
                        $arr = [
                            1,
                            2,
                            3
                        ];
                        EXPECTED,
                    <<<'INPUT'
                        <?php
                        $arr = [1, 2, 3
                                    ];
                        INPUT,
                ],

                'multiline nested array gets formatted.' => [
                    <<<'EXPECTED'
                        <?php
                        $arr = [
                            1,
                            2,
                            3 => [
                                "one" => 1
                            ]
                        ];
                        EXPECTED,
                    <<<'INPUT'
                        <?php
                        $arr = [1, 2, 3 => [
                                    "one" => 1 ]
                                    ];
                        INPUT,
                ],

                'Multiline nested array with siblings gets formatted.' => [
                    <<<'EXPECTED'
                        <?php
                        $arr = [
                            "a" => [
                                "a_1" => "val_a"
                            ],
                            "b" => [
                                "b_1" => "val_b"
                            ],
                            "c" => [
                                "c_1" => "val_c"
                            ]
                        ];
                        EXPECTED,
                    <<<'INPUT'
                        <?php
                        $arr = [
                            "a" => [ "a_1" => "val_a"
                        ],
                        "b" => [
                                "b_1" => "val_b" ],
                                        "c" => [
                                            "c_1" => "val_c" ]
                        ];
                        INPUT,
                ],

                'Multiline deeply nested array gets formatted.' => [
                    <<<'EXPECTED'
                        <?php
                        $arr = [
                            "level_1" => [
                                "level_2" => [
                                    "level_3" => [
                                        "level_4" => [
                                            "level_5" => [
                                                "level_6" => [
                                                    "level_7" => [
                                                        "level_8" => [
                                                            "level_9" => [
                                                                "level_10" => [
                                                                    "deep_value" => "You found me!",
                                                                    "deep_array" => [1, 2, 3, 4, 5],
                                                                    "deep_assoc" => ["key" => "value"],
                                                                    "deep_bool" => true,
                                                                    "deep_null" => null,
                                                                    "deep_float" => 3.14159
                                                                ]
                                                            ]
                                                        ]
                                                    ],
                                                    "branch_a" => [
                                                        "sub_branch_1" => [
                                                            "sub_sub_1" => [
                                                                "data" => "nested data 1",
                                                                "number" => 42,
                                                                "active" => true,
                                                            ],
                                                        ],
                                                    ],
                                                ],
                                            ],
                                        ],
                                    ],
                                ],
                            ],
                        ];
                        EXPECTED,
                    <<<'INPUT'
                        <?php
                        $arr = [
                            "level_1" => [
                              "level_2" => [
                            "level_3" => [
                                "level_4" => [
                              "level_5" => [
                                    "level_6" => [
                                  "level_7" => [
                                "level_8" => [
                                    "level_9" => [
